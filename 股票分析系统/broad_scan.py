def fetch_kline(code):
    """获取单只股票K线"""
    sym = f"sh{code}" if code.startswith("6") or code.startswith("9") else f"sz{code}"
    try:
        r = requests.get(
            "https://web.ifzq.gtimg.cn/appstock/app/fqkline/get",
            params={"param": f"{sym},day,,,120,qfq"},
            headers={"User-Agent": "Mozilla/5.0"}, timeout=10
        ).json()
        kls = r["data"][sym]["qfqday"]
    except:
        try:
            sym2 = sym.replace("sh","X").replace("sz","sh").replace("X","sz")
            r = requests.get(
                "https://web.ifzq.gtimg.cn/appstock/app/fqkline/get",
                params={"param": f"{sym2},day,,,120,qfq"},
                headers={"User-Agent": "Mozilla/5.0"}, timeout=10
            ).json()
            kls = r["data"][sym2]["qfqday"]
        except:
            return None
    closes = []
    for k in kls:
        closes.append(float(k[2]))
    rets = [(closes[i]/closes[i-1]-1)*100 for i in range(1, len(closes))]
    return np.array(rets), [k[0] for k in kls[1:]]

