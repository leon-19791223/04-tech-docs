def scan_stock(code):
    name = STOCK_POOL[code]
    data = fetch_kline(code)
    if data is None:
        return None
    rets, _ = data
    if len(rets) < 15:
        return None
    # 对齐天数
    min_len = min(len(yt_ret), len(rets))
    ya = yt_ret[-min_len:]
    sa = rets[-min_len:]
    corr = np.corrcoef(ya, sa)[0, 1]
    if corr > 0.35:
        same_dir = np.mean(((ya>0)&(sa>0))|((ya<0)&(sa<0)))*100
        r3m = (1+sa.sum()/100)*100-100  # rough return
        return {"code": code, "name": name,
                "corr": round(corr, 4), "same_dir": round(same_dir, 1),
                "ret_3m": round(r3m, 2), "days": min_len}
    return None

# 使用线程池加速
with concurrent.futures.ThreadPoolExecutor(max_workers=10) as executor:
    futures = {executor.submit(scan_stock, code): code for code in codes_list}
    done = 0
    for future in concurrent.futures.as_completed(futures):
        done += 1
        result = future.result()
        if result:
            results.append(result)
        if done % 100 == 0 or done == total:
            print(f"  进度: {done}/{total}, 已发现 {len(results)} 只")

